function back1(){
    if($(window).width() < 700){
    var ind = document.getElementById("index");
    var back = document.getElementById("back");
        if (ind.style.display === 'grid'){
            document.getElementById('index').scrollTo(0,0);
            ind.style.display = 'none';
            back.style.display = 'none';
            $('.grid').stop(true,true).fadeOut(800).css('display', 'none');
            $('.gifphoto').stop(true,true).fadeOut(800);
            $('.slide').stop(true,true).show()
            $('.categories').stop(true,true).fadeIn();
            $('.catphoto').stop(true,true).fadeIn(300).css('display', 'block');
            $('.subtitle').stop(true,true).fadeOut(800);
            $('.top').stop(true,true).fadeOut(800);
            $('.top2').stop(true,true).fadeOut(800);
            $('.btm').stop(true,true).fadeOut(800);
            $('.no').stop(true,true).fadeOut(800);

        } else if ($('.hi').css('display') != 'none'){
            window.location.reload();
        } else {
            $('.no').stop(true,true).fadeOut();
            $('#expcarousel').fadeOut(800).css('display', 'none');
            $('#impcarousel').fadeOut(800).css('display', 'none');
            $('#textcarousel').fadeOut(800).css('display', 'none');
            $('#colcarousel').fadeOut(800).css('display', 'none');
            $('#curcarousel').fadeOut(800).css('display', 'none');
            ind.style.display = "grid";
        }
    } else {
        $('#back').stop(true,true).css('display', 'none');
        $('#back1').stop(true,true).css('display', 'none');
    }
  }

